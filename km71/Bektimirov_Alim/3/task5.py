#програма для визначення попереднього і наступного значення числа
number = int(input("Введіть число"))
#наступне число
nextnumber = number + 1
#попереднэ число
prenumber = number - 1
#виведення результату
print("The next number for the number",number,"is",nextnumber)
print("The previous number for the number",number,"is",prenumber)
