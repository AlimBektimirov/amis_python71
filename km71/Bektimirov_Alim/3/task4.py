# Програма для підрахунка кількості яблук для кожного і остачі
# кількість студентів
student = int(input("Введіть кількість студентів: "))
if student == 0:
    print("Немає кому їсти яблуки")
# кількість яблук
apples = int(input("Введіть кількість яблук: "))
# кількість кожному
print("Кожному студенту дістанеться",str(apples // student),"яблук")
# кількість залишку
print("В кошику залишиться",str(apples % student),"яблук")
