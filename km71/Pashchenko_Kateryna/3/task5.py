# Програма для знаходження сусідніх чилел для заданого числа
n = int(input('Enter number: '))
ans1 = n + 1#наступне число пісял заданого
ans2 = n - 1#попереднє число підсля заданого
print('The next number for the number',(n),'is',(ans1),'\n','The previos number for the number', (n), 'is',(ans2))
