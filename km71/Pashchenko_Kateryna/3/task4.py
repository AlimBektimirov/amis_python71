#Програма для підрахунку яблук для кожного студента і остачі яблук
N = int(input('Enter number of students:'))#кількість студентві
K = int(input('Enter number of apples:'))#кількість яблук
res1 = N // K#кількість яблук для одного студента
res2 = N%K#кількість яблук, що залишилися
print((res1),'\n',(res2))
