# Програма для знаходження найменшої кількості столів
g1 = int(input('Кількість учнів групи №1:'))
g2 = int(input('Кількість учнів групи №2:'))
g3 = int(input('Кількість учнів групи №3:'))
ans1 = round(g1 / 2)# кількість стодів для першої групи
ans2 = round(g2 / 2)# кількість столів для другої групи
ans3 = round(g3 / 2)# кількість столів для третьої групи
print( ans1 + ans2 + ans3)
